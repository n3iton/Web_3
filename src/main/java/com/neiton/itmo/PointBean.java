package com.neiton.itmo;


public class PointBean {

  private double X;
  private double Y;
  private double R;

  public double getX() {
    return X;
  }

  public void setX(double x) {
    X = x;
  }

  public double getY() {
    return Y;
  }

  public void setY(double y) {
    Y = y;
  }

  public double getR() {
    return R;
  }

  public void setR(double r) {
    R = r;
  }
}
